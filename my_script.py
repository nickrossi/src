# Integer variables
age = 25
num_apples = 10

print("Age:", age)
print("Number of apples:", num_apples)

# Float variables
pi = 3.14159
temperature = 98.6

print("Pi:", pi)
print("Temperature:", temperature)

# String variables
name = "Alice"
greeting = "Hello, world!"

print("Name:", name)
print("Greeting:", greeting)

# List variables
fruits = ['apple', 'banana', 'cherry']
scores = [85, 90, 75, 88]

print("Fruites:", fruits)
print("Scoress:", scores)

# Tuple variables
coordinates = (10, 20)
rgb_values = (255, 128, 0)

print("Coordinates:", coordinates)
print("RGB Values:", rgb_values)

# Dictionary variables
person = {'name': 'Bob', 'age': 30, 'city': 'New York'}
grades = {'math': 95, 'science': 88, 'history': 92}

print("Person:", person)
print("Grades:", grades)

# Boolean variables
is_python_fun = True
is_learning = False

print("Is Python fun?", is_python_fun)
print("Is learning?", is_learning)

# NoneType variable
no_value = None
foo = None

print("No value:", no_value)
print("Foo:", foo)